    private void search_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_buttonActionPerformed
        String query = search_bar.getText();
        if (isInt(query)) {
            String name = "";
            String uni = "";
            String result = "";
            String desc = "";
            String numbers = "";
            FindID temp = new FindID(Integer.parseInt(query));
            for (int i = 0; i < FindID.retUser().length; i++) {
                if (i == 0 || i == 1) {
                    name += FindID.retUser()[i] + " ";
                    continue;
                }
                if (i == 2) {
                    uni += FindID.retUser()[i];
                    continue;
                }
                if (i == 3) {
                    desc += FindID.retUser()[i];
                    continue;
                }
                if (i==FindID.retUser().length-3 || i==FindID.retUser().length-2){
                    numbers += FindID.retUser()[i] + ",";
                    continue;
                }
                if (i== FindID.retUser().length-1){
                    continue;
                }
                else{
                    result += FindID.retUser()[i] + ",";
                }
            }
            output_box.setText("Name: " + name + "\nUniversity: " + uni + "\nResults: " + result + "\nDescription: " + desc + "\nPhone & #Roomy: " + numbers);
        }
        if (query.equals("users")) {

            File folder = new File(FindID.FILE_LOC);
            File[] listOfFiles = folder.listFiles();
            String uIDs[] = new String[listOfFiles.length];
            for (int i = 0; i < listOfFiles.length; i++) {
                if (listOfFiles[i].isFile()) {
                    uIDs[i] = listOfFiles[i].getName();
                }
            }
            String temp = "";
            for (int i = 1; i < uIDs.length; i++) {
                temp += uIDs[i] + "\n";
            }
            output_box.setText(temp);
        }
        cls();
    }//GEN-LAST:event_search_buttonActionPerformed

