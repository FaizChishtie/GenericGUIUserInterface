    private void enter_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter_buttonActionPerformed
        //this button stores people as newStudents
        String fname, lname, uni, desc;
        String[] temp_res;
        int phone, num_roomies;
        fname = first_name.getText();
        lname = last_name.getText();
        uni = university.getText();
        desc = description.getText();
        temp_res = results.getText().split(",");
        phone = Integer.parseInt(phone_number.getText());
        num_roomies = Integer.parseInt(number_roomies.getText());
        double[] result = new double[temp_res.length];

        for (int i = 0; i < temp_res.length; i++) {
            result[i] = Double.parseDouble(temp_res[i]);
        }

        newStudent temp = new newStudent(fname, lname, uni, desc, result, phone, num_roomies);

        try {
            newStudent.initializeStudent(temp);
        } catch (IOException e) {
            System.err.println("IOException: " + e);
        }

        output_box.setText("User " + temp.first_name + " " + temp.last_name + " Stored! \n" + "CSV File: " + temp.userID + ".csv \n@: " + newStudent.FILE_LOC);
        cls();

    }//GEN-LAST:event_enter_buttonActionPerformed

